                <p>
                    Child marriage is a commonpractice in Nigeria.<br>

                    For thousands of years, millions of Nigerian girls have been told they 
                    belong to the kitchen and ‘the other room.’ Girls as young as 9 years 
                    have been pulled out of school, and introduced to men – often double their age, 
                    as their husband. Obviously, the aftermath is disastrous. In addition, Nigeria is 
                    predominantly a patriarchal society, making it extremely difficult for girls to 
                    acquire secondary or tertiary education, or aspire for high positions in 
                    government or the corporate sector.<br>
                </p>
                <p>
                    Child marriage is described as ‘a young human being below the age of puberty or 
                    below the legal age of majority’.  In addition, Section 23 of the Constitution 
                    of the Federal Republic of Nigeria says “a person under the age of 18 is 
                    incapable of contracting a valid marriage. If such a marriage does take 
                    place, it should be declared null and void and of no effect.” Contradictorily, 
                    Section 29(4b) of the same constitution technically approves 
                    child marriage. United Nations Children’s Fund, “marriage before the age of 18 is a 
                    fundamental violation of human rights. Child marriage decreases a girl’s development 
                    by resulting in early pregnancy and social isolation. It interrupts her education, 
                    limiting her opportunities for career and vocational advancement, placing her 
                    at increased risk of domestic violence, and exposing her to life threatening illnesses[iii].” Some of the key ailments child brides are exposed to include Vesico-vaginal Fistula (VVF), Anaemia, High Blood Pressure (HBP), Premature Birth, Malnutrition, Sexually Transmitted Diseases (STDs) and Postpartum depression (PPD), and even suicide[iv].
                    <br>
                    Fistula is reportedly one of the worst epidemics predominantly caused by 
                    child marriages. According to the international fistula foundation, an 
                    obstetric fistula is a hole between the vagina and rectum or bladder 
                    that is caused by prolonged obstructed labour, leaving a woman incontinent 
                    of urine or faeces or both. Over time, it leads to chronic medical problems 
                    like frequent infections, kidney disease and infertility[v]. Medically, when 
                    an under-aged girl has sex, gets pregnant and goes through childbirth, because 
                    her body is not developed enough for child bearing, she is highly exposed 
                    to a fistula. It was revealed that 65% of all cases of obstetric fistula 
                    occur in girls under the age of 18. It is also revealed 
                    that ‘girls who give birth before the age of 15 are five times more likely to 
                    die in childbirth than girls in their 20s’’
                </p>
                <p>
                    Out of the top 20 countries with the highest rates of child marriage across the 
                    globe, 17 are African countries, and Nigeria ranks at number 11. In neighbouring 
                    West African country, Niger, 76% of girls are married before the age of 18, 
                    while in Central African Republic the figure stands at 68%[vii]. In Nigeria, 
                    child marriage has ancient cultural, religious and economic ties. It is believed 
                    that girls who marry after the age of 18 can become promiscuous, while other families 
                    give out their daughters to ‘escape poverty’

                    According to a 2017 report by UNICEF, 43% of Nigerian girls are married off 
                    before their 18th birthday, while 17% are married before they 
                    turn 15[  The prevalence of child marriage in Nigeria varies from 
                    region to region, with figures as high as 76% in the North Western 
                    region and as low as 10% in the South Eastern region.

                    The underlying fact remains that child marriage must be completely 
                    abolished in Nigeria, it is one of the greatest social injustices of 
                    our time. Child marriage is child abuse as it has severe and negative 
                    psycho-social effects on their victims. Sexual acts on children can 
                    cause both physical and mental trauma which leaves indelible scars, 
                    that take years, and sometimes decades to eliminate.

                    It is highly imperative that the Nigerian Government takes immediate action to 
                    provide adequate security to all Government schools, particularly where the 
                    lives of children are involved.  The lives of young children, particularly 
                    young girls must not be used as pawns for political discourse, conflict 
                    entrepreneurship, or gambling.
                </p>
                <p>
                    “No child or indeed any other Nigerian should be put through the 
                    brutality of abduction, violence, or forced marriage. Every girl 
                    has a right to education and choice of life.” We stand against 
                    child marriage and forceful marriage and we have gone far in 
                    educating parents in many communities in Nigeria about the problem 
                    of such act. Any issue or case of such that is reported to our 
                    office is handled with serious legal action against the offender.
                </p>
