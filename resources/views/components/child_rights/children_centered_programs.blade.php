                    Our key role of helping the  needy children is to tackle poverty which does not lie in doling out benefits to them. We focused on equipping them with all the necessary tools (quality education, healthcare, safety and security, professional skills, etc.) so that they can attain personal economic growth and in turn contribute to their own, society’s and country’s growth.
