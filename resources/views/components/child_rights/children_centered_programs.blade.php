                <p>
                    Child abuse includes any act or failure to act by a parent or a caregiver that results 
                    in actual or potential harm to a child, and can occur in a child's home, or in 
                    the organizations, schools or communities the child interacts with. All children have 
                    the right to be protected from violence, exploitation and abuse. Yet, millions of 
                    children worldwide from all socio-economic backgrounds, across all ages, religions 
                    and cultures suffer violence, exploitation and abuse every day. Millions more are at risk.
                </p>
                <p>
                    Some girls and boys are particularly vulnerable because of gender, age, race, ethnic origin or socio-economic status. Higher levels of vulnerability are often associated with children with disabilities who are orphaned,
                    indigenous, from ethnic minorities and other marginalised groups. Other risks for children are associated with living and working on the streets and living in communities where inequality, unemployment and poverty are highly concentrated. 
                    Natural disasters, armed conflict, and displacements may expose children to additional risks.</p>
                <p>
                    Violence, exploitation and abuse are often practiced by someone known to the child, including parents, other family members, caretakers, teachers, customers (for children that hawks) and street children.
                </p>
                <p>
                    There is significant evidence that violence, exploitation and abuse can affect the child’s physical and mental health in the short and longer term, impairing their ability to learn and socialize, and impacting their transition to adulthood with adverse consequences later in life.
                    In Nigeria, there are many cases of domestic violence or abuses against children in Nigeria. In Africa, there is this common belief that for a child to grow into responsible adult in future, every measure of beatings must be used
                </p>
