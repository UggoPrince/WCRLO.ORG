                <p>
                    Female genital mutilation (FGM) includes procedures that intentionally alter or cause injury to the female genital organs for non-medical reasons. The practice has no health benefits for girls and women.
                    Procedures can cause severe bleeding and problems urinating, and later cysts, infections, as well as complications in childbirth and increased risk of newborn deaths.
                    Female Genital Mutiliation is mostly carried out on young girls between infancy and age 15 and it is a violation of the human rights of girls and women.
                    Female genital mutilation (FGM) comprises all procedures that involve partial or total removal of the external female genitalia, or other injury to the female genital organs for non-medical reasons.
                </p>
                <p>
                    The practice is mostly carried out by traditional circumcisers, who often play 
                    other central roles in communities, such as attending childbirths. In many 
                    settings, health care providers perform FGM due to the erroneous belief that 
                    the procedure is safer when medicalized1. WHO strongly urges health professionals 
                    not to perform such procedures.

                    This practice is recognized internationally as a violation of the human rights 
                    of girls and women. It reflects deep-rooted inequality between the sexes, and 
                    constitutes an extreme form of discrimination against women. It is nearly 
                    always carried out on minors and is a violation of the rights of 
                    children. The practice also violates a person's rights to health, security 
                    and physical integrity, the right to be free from torture and cruel, inhuman 
                    or degrading treatment, and the right to life when the procedure results in death.

                    There are other such practises, e.g. pricking, piercing, incising, scraping and 
                    cauterizing the genital area. We discourage such harmful practises among mothers 
                    and the communities, and whoever found indulging any child on any form 
                    of Genital Mutiliation will be prosecuted.<br>
                    <span class=" text-primary"> WCRLO</span> has been running campaigns, holding 
                    seminars and workshops educating parents against such practises. The cases of FGM is 
                    reducing and we are not relenting untill it is abolished in Nigeria.

