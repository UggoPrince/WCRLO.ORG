                <p>
                    Violence against children has lifelong impacts on health and well-being 
                    of children, families, communities, and nations. Violence against children can:
                </p>
                <ul class="">
                    <li>
                    Result in death. Homicide, which often involves weapons such as knives and 
                    firearms, is among the top three causes of death in adolescents, with boys 
                    comprising over 80% of victims and perpetrators.
                    </li>
                    <li>
                    Lead to severe injuries. For every homicide, there are hundreds of predominantly 
                    male victims of youth violence who sustain injuries because of physical fighting and assault.
                    </li>
                    <li>
                    Impair brain and nervous system development. Exposure to violence at an 
                    early age can impair brain development and damage other parts of the nervous 
                    system, as well as the endocrine, circulatory, musculoskeletal, reproductive, 
                    respiratory and immune systems, with lifelong consequences. As such, violence 
                    against children can negatively affect cognitive development and results in 
                    educational and vocational under-achievement.
                    </li>
                    <li>
                    Result in negative coping and health risk behaviours. Children exposed 
                    to violence and other adversities are substantially more likely to smoke, 
                    misuse alcohol and drugs, and engage in high-risk sexual behaviour. They also 
                    have higher rates of anxiety, depression, other mental health problems and suicide.
                    </li>
                    <li>
                    Lead to unintended pregnancies, induced abortions, gynaecological problems, and sexually 
                    transmitted infections, including HIV contribute to a wide range of non-communicable 
                    diseases as children grow older. The increased risk for cardiovascular 
                    disease, cancer, diabetes, and other health conditions is largely due to 
                    the negative coping and health risk behaviours associated with violence.
                    </li>
                    <li>
                    Impact opportunities and future generations. Children exposed to violence 
                    and other adversities are more likely to drop out of school, have difficulty 
                    finding and keeping a job, and are at heightened risk for later victimization 
                    and/or perpetration of interpersonal and self-directed violence, by which 
                    violence against children can affect the next generation.
                    </li>
                </ul>
                <p>
                    Violence against children is a multifaceted problem with causes at the 
